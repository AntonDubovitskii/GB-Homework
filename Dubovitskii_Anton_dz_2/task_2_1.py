print((15 * 3), type(15 * 3))            # 45 <class 'int'>
print(isinstance((15 * 3), int))         # True

print((15 / 3), type(15 / 3))            # 5.0 <class 'float'>
print(isinstance((15 / 3), float))       # True

print((15 // 2),  type(15 // 2))         # 7 <class 'int'>
print(isinstance((15 // 2), int))        # True

print((15 ** 2),  type(15 ** 2))         # 225 <class 'int'>
print(isinstance((15 ** 2), int))        # True
